class I18nService {
  private currentLanguage: Language = "en";

  constructor() {
    this.loadSavedLanguage();
  }

  /**
   * Set current language
   */
  setLanguage(language: Language): void {
    if (translations[language]) {
      this.currentLanguage = language;
      localStorage.setItem("language", language);
    }
  }

  /**
   * Get current language
   */
  getLanguage(): Language {
    return this.currentLanguage;
  }

  /**
   * Get all available languages
   */
  getAvailableLanguages(): Array<{ code: Language; name: string }> {
    return [
      { code: "en", name: "English" },
      { code: "es", name: "Español" },
      { code: "fr", name: "Français" },
      { code: "de", name: "Deutsch" },
      { code: "pt", name: "Português" },
      { code: "ja", name: "日本語" },
      { code: "zh", name: "中文" },
      { code: "ru", name: "Русский" },
    ];
  }

  /**
   * Translate key
   */
  t(key: string): string {
    const keys = key.split(".");
    let value: any = translations[this.currentLanguage];

    for (const k of keys) {
      if (value[k]) {
        value = value[k];
      } else {
        return key; // Return key if translation not found
      }
    }

    return typeof value === "string" ? value : key;
  }

  /**
   * Format number
   */
  formatNumber(num: number): string {
    return new Intl.NumberFormat(this.currentLanguage).format(num);
  }

  /**
   * Format currency
   */
  formatCurrency(num: number, currency: string = "USD"): string {
    return new Intl.NumberFormat(this.currentLanguage, {
      style: "currency",
      currency: currency,
    }).format(num);
  }

  /**
   * Format date
   */
  formatDate(date: Date): string {
    return new Intl.DateTimeFormat(this.currentLanguage).format(date);
  }

  /**
   * Load saved language from localStorage
   */
  private loadSavedLanguage(): void {
    const saved = localStorage.getItem("language") as Language;
    if (saved && translations[saved]) {
      this.currentLanguage = saved;
    }
  }
}

export const i18n = new I18nService();
export default i18n;
